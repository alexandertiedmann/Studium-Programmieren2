package compare;

/**
 * class Integer gets a value und can return it
 * @author Alexander Tiedmann (s0556127)
 * @version 1.0
 * @since 29.11.2016
 */
public class Integer{
    private int value;
    /**
     * constructor for Integer
     * @param v value
     */
    public Integer (int v){
        value = v;
    }

    /**
     * gets the value
     * @return value
     */
    public int getValue(){
        return value;
    }

}
